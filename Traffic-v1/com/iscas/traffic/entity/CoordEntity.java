package com.iscas.traffic.entity;

public class CoordEntity {
    private String roadId;
    private String roadname;
    private String lng;
    private String lat;

    public CoordEntity() {
    }

    public CoordEntity(String pointcode, String pointName, String longitude, String latitude) {
        this.roadId = pointcode;
        this.roadname = pointName;
        this.lng = longitude;
        this.lat = latitude;
    }

    public String getRoadId() {
        return roadId;
    }

    public void setRoadId(String roadId) {
        this.roadId = roadId;
    }

    public String getRoadname() {
        return roadname;
    }

    public void setRoadname(String roadname) {
        this.roadname = roadname;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    @Override
    public String toString() {
        return "CoordEntity{" +
                "roadId='" + roadId + '\'' +
                ", roadname='" + roadname + '\'' +
                ", lng='" + lng + '\'' +
                ", lat='" + lat + '\'' +
                '}';
    }
}
